function countKeys(obj) {
  let n = 0
  for (const k of Object.keys(obj)) {
    if (obj[k] && typeof obj[k] === 'object' && !Array.isArray(obj[k])) n += countKeys(obj[k])
    else n++
  }
  return n
}

function writeLocale(lang, name, data) {
  const p = path.join(localesDir, lang, `${name}.json`)
  fs.writeFileSync(p, JSON.stringify(data, null, 2) + '\n', 'utf8')
  console.log(`${lang}/${name}.json keys: ${countKeys(data)}`)
}

