function deepMergeTh(en, thMap) {
  const out = JSON.parse(JSON.stringify(en))
  function walk(target, map, prefix = '') {
    for (const k of Object.keys(target)) {
      const key = prefix ? `${prefix}.${k}` : k
      if (
        target[k] &&
        typeof target[k] === 'object' &&
        !Array.isArray(target[k])
      ) {
        walk(target[k], map, key)
      } else if (map[key] !== undefined) {
        target[k] = map[key]
      }
    }
  }
  walk(out, thMap)
  return out
}

